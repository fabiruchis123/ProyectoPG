    public void iniTablero(){
        
           for (int i = 0; i < 12; i++) {
                  for (int j = 0; j < 12; j++) {
                      if (i==5 || i ==6 && j==5 || j==6) {
                          if (TableroInicial[i][j] == 1) {
                                this.botones[i][j].setEnabled(true);
                                this.botones[i][j].setIcon(icon1mora);
                                continue;
                           }else if (TableroInicial[i][j]== 2) {
                                this.botones[i][j].setEnabled(true);
                                this.botones[i][j].setIcon(icon2negro);
                                continue;
                        }
                      }
                            this.botones[i][j].setIcon(iconBlanco);
                            this.botones[i][j].setOpaque(true);
                            this.botones[i][j].setEnabled(true);
                                        
               } 
               }
               
               
        
    }
    private void reiniciarJuego(){
        int opcion  = JOptionPane.showConfirmDialog(this,"Desea reiniciar el juego?", "Reiniciar Juego",JOptionPane.YES_NO_OPTION);
        if (opcion==JOptionPane.YES_NO_OPTION) {
            juego.reiniciarJuego();
            juga1.setCantFichas(2);
            juga2.setCantFichas(2);
            lblFichas1.setText("2");
            lblFichas2.setText("2");
            jugActual = juga1;
            turno.setText(jugActual.getNombre());
        
        // Actualizar la interfaz gráfica
            iniTablero();
        }
    }
    public void reiniciarTablero() {
    juego.inicializarTablero();
    iniTablero();
    }
